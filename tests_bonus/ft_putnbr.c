#include <unistd.h>

void	ft_putnbr(int n)
{
	static char	buf[11];
	int			neg;
	int			i;

	neg = (n < 0);
	if (n > 0)
		n = -n;
	i = 10;
	buf[i] = '0' - (n % 10);
	n /= 10;
	while (n)
	{
		buf[--i] = '0' - (n % 10);
		n /= 10;
	}
	if (neg)
		buf[--i] = '-';
	write(1, buf + i, 11 - i);
}
